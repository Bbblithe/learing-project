//学生类型 这是引用数据类型

//定义一个类，类名student
//Student是一个类，代表了所有的学生对象，是一个学生模版。
public class Student{	//定义一个公开的类，起名student

	//属性【描述的是对象的状态信息】
	//属性通常采用的是变量的方式来定义
	//类体当中，放大体之外定义的变量被称为“成员变量”
	//成员变量没有赋值，系统赋默认值：一切向0看齐

	//学号
	int no;

	//姓名
	String name;

	//性别
	boolean sex;

	//年龄
	int age;

	//住址
	String address;

	//方法
	//方法描述的是对象的动作信息
	//当前例子就是描述属性，不描述方法。
}